func TestCreateTable_emitsIdentityClause(t *testing.T) {
	t1 := &schema.Table{Schema: "public", Name: "t", Columns: []*schema.Column{
		{Name: "id", TypeSQL: "bigint", NotNull: true, Identity: "by-default", IsPrimaryKey: true},
		{Name: "email", TypeSQL: "text", NotNull: true},
	}}
	sql := createTableSQL(t1)
	assert.True(t, strings.Contains(sql, "GENERATED BY DEFAULT AS IDENTITY"), "expected IDENTITY clause in DDL: %q", sql)
	// IDENTITY implies NOT NULL — confirm we don't double-emit:
	idCount := strings.Count(sql, "id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY")
	assert.Equal(t, 1, idCount, "expected single IDENTITY+PK form for id column; got:\n%s", sql)
}

func TestIdentityClause(t *testing.T) {
	assert.Equal(t, "ALWAYS", identityClause("always"))
	assert.Equal(t, "BY DEFAULT", identityClause("by-default"))
	assert.Equal(t, "", identityClause(""))
}
