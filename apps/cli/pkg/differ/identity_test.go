func TestDiffIdentity_addToPlainColumn(t *testing.T) {
	des := &schema.SchemaState{Tables: map[string]*schema.Table{
		"public.t": {Schema: "public", Name: "t", Columns: []*schema.Column{
			{Name: "id", TypeSQL: "bigint", NotNull: true, Identity: "by-default"},
		}},
	}}
	live := &schema.SchemaState{Tables: map[string]*schema.Table{
		"public.t": {Schema: "public", Name: "t", Columns: []*schema.Column{
			{Name: "id", TypeSQL: "bigint", NotNull: true},
		}},
	}}
	dr, err := Diff(des, live, Options{})
	require.NoError(t, err)
	var saw bool
	for _, s := range dr.Plan.Statements {
		if s.OpType == "ADD_IDENTITY" {
			saw = true
			assert.Contains(t, s.DDL, "ADD GENERATED BY DEFAULT AS IDENTITY")
		}
	}
	assert.True(t, saw, "expected ADD_IDENTITY")
}

